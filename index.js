document
  .getElementById("completed-1")
  .addEventListener("click", function (event) {
    let taskNumber = document.getElementById("task_n").innerText;
    let convertedTaskNumber = parseFloat(taskNumber);
    let taskSum = convertedTaskNumber - 1;
    document.getElementById("task_n").innerText = taskSum;

    let navTask = document.getElementById("nav_task").innerText;
    let convertedNavTask = parseInt(navTask);
    let navSum = convertedNavTask + 1;
    document.getElementById("nav_task").innerText = navSum;

    document.getElementById("completed-1").style.backgroundColor = "#8D8E9A";

    alert("Board updated successfully");

    // add box

    let box = document.createElement("div");
    box.style.width = "300px";
    box.style.height = "100px";
    box.style.backgroundColor = "#F4F7FF";
    box.style.margin = "25px";
    box.style.borderRadius = "15px";
    box.textContent =
      `You have Complete The Task Fix Mobile Button Issue at ` +
      new Date().toLocaleTimeString();
    box.style.color = "gray";
    box.style.display = "flex";
    box.style.alignItems = "center";
    box.style.justifyContent = "center";
    box.style.textAlign = "center";
    document.getElementById("container").appendChild(box);
  });

