const colors = ["#747EC2", "#B874C2", "#74C2A3", "#C274A7", "#C27475"];
let colorIndex = 0;
document.getElementById('colorButton').addEventListener('click', 
    function (event) {
        document.body.style.backgroundColor = colors[colorIndex];
        colorIndex = (colorIndex + 1) % colors.length;
    }
);
